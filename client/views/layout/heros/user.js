Template.heroUser.onCreated(function() {
  let instance = this;

  // Meteor.subscribe("user-profile", FlowRouter.getParam('user_id'));

  instance.user = new ReactiveVar(Meteor.users.findOne({_id: FlowRouter.getParam('user_id')}))

  // Particle JS
  initParticlesJS()
})

Template.heroUser.helpers({
  getUser() {
    return Template.instance().user.get()
  },
  getTitle() {
    let user = Template.instance().user.get()
    return getDisplayName(user) + "'s profile"
  },
})

let initParticlesJS = function() {
  // Html not loaded, wait 100ms
  if (!$('#particles-js').length) {
    return Meteor.setTimeout(initParticlesJS, 100)
  }
  particlesJS('particles-js', particles_json)
}

